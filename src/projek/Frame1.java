    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(eNama.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap inputkan nama Anda");
        }else if(eAlamat.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap inputkan alamat Anda");
        }else if(ePB.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap inputkan paket berat Anda");
        }else if(NOP.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Harap inputkan no HP Anda");
        }

        String nama = eNama.getText();
        String alamat = eAlamat.getText();
        String berat = ePB.getText() + " kg";

        int hitung = Integer.parseInt(ePB.getText());
        String nohp = NOP.getText();
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd.MM.yyyy");
        String tgl = dateFormat.format(TGL.getDate());
        String paket;
        int harga = 0;

        if(rb1.isSelected())
        paket = "Cuci Kering";
        else if (rb2.isSelected())
        paket = "Cuci Kering Setrika";
        else if (rb3.isSelected())
        paket = "Setrika";
        else
        paket = "";

        if (rb1.isSelected())
        harga = hitung*3000;
        if (rb2.isSelected())
        harga = hitung*4000;
        if (rb3.isSelected())
        harga = hitung*2000;

        if(!(rb1.isSelected() || rb2.isSelected() || rb3.isSelected() )){
            JOptionPane.showMessageDialog(null, "Harap pilih paket layanan");
        }else{
            JOptionPane.showMessageDialog(null, "Data Anda telah lengkap. Tekan OK untuk melihat harga seluruhnya");
            new Frame2(nama, alamat, berat, nohp,tgl, paket, harga).setVisible(true);
            dispose();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

